/** 获取指定分类的题目 */
export const getQuizzesByCategory = (category: QuizCategory): QuizQuestion[] => {
  return quizzes.filter(q => q.category === category);
};

/** 获取指定难度的题目 */
export const getQuizzesByDifficulty = (difficulty: QuizDifficulty): QuizQuestion[] => {
  return quizzes.filter(q => q.difficulty === difficulty);
};

/** 随机获取指定数量的题目 */
export const getRandomQuizzes = (count: number, category?: QuizCategory): QuizQuestion[] => {
  const pool = category ? getQuizzesByCategory(category) : quizzes;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

