/** 根据 ID 查找品种 */
export const getBreedById = (id: string): Breed | undefined => {
  return breeds.find(b => b.id === id);
};

/** 根据物种筛选品种列表 */
export const getBreedsBySpecies = (species: Species): Breed[] => {
  return breeds.filter(b => b.species === species);
};
