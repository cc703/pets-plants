function buildGallery(breed: Breed): string[] {
  const pool = breed.species === 'cat' ? CAT_GALLERY_POOL : DOG_GALLERY_POOL;
  const start = Math.max(0, breed.popularityRank - 1) % pool.length;
  const rotated = [...pool.slice(start), ...pool.slice(0, start)];
  return Array.from(new Set([breed.imageUrl, ...rotated])).slice(0, 5);
}

